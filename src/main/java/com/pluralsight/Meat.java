package com.pluralsight;

public class Meat {
    String steak;
    String ham;
    String salami;
    String roastBeef;
    String chicken;
    String bacon;

    public Meat(String steak, String ham, String salami, String roastBeef, String chicken, String bacon) {
        this.steak = steak;
        this.ham = ham;
        this.salami = salami;
        this.roastBeef = roastBeef;
        this.chicken = chicken;
        this.bacon = bacon;
    }
}
